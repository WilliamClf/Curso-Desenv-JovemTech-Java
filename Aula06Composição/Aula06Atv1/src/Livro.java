public class Livro {
    private Autor autor;
    private Editora editora;
    private Capitulos capítulos;
    private Titulo título;

    public Autor getAutor() {
        return autor;
    }

    public void setAutor(Autor autor) {
        this.autor = autor;
    }

    public Editora getEditora() {
        return editora;
    }

    public void setEditora(Editora editora) {
        this.editora = editora;
    }

    public Capitulos getCapítulos() {
        return capítulos;
    }

    public void setCapítulos(Capitulos capítulos) {
        this.capítulos = capítulos;
    }

    public Titulo getTítulo() {
        return título;
    }

    public void setTítulo(Titulo título) {
        this.título = título;
    }
}
