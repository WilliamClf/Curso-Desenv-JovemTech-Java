public class Gramado {
    String tamanhoM;
    String cor;
    String umidade;

    Gramado() {
        this.tamanhoM = "";
        this.cor = "";
        this.umidade = "";
    }

    Gramado(String tamanhoM, String cor, String umidade) {
        this.tamanhoM = tamanhoM;
        this.cor = cor;
        this.umidade = umidade;
    }
}
