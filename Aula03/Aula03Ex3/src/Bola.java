public class Bola {
    double tamanho;
    String cor;
    double peso;

    Bola() {
        this.tamanho = 0.0;
        this.cor = "";
        this.peso = 0.0;
    }

    Bola(double tamanho, String cor, double peso) {
        this.tamanho = tamanho;
        this.cor = cor;
        this.peso = peso;
    }
}
