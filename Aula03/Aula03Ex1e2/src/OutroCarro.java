public class OutroCarro {
    String cor;
    String tipo;
    String placa;
    String marca;
    String modelo;
    int anoFabricação;
    String tipoCombustivel;

    OutroCarro(String cor, String tipo, String placa, String marca, String modelo,
            int anoFabricação, String tipoCombustivel) {
        this.cor = cor;
        this.tipo = tipo;
        this.placa = placa;
        this.marca = marca;
        this.modelo = modelo;
        this.anoFabricação = anoFabricação;
        this.tipoCombustivel = tipoCombustivel;
    }

}
